    public void readJTable() {
         DefaultTableModel modelo = (DefaultTableModel) jTableProduto.getModel();
         modelo.setNumRows(0);
         ProdutoDAO pdao = new ProdutoDAO();
         
         for(Produto prod: pdao.read()){
             modelo.addRow(new Object[] {
                 prod.getId(),
                 prod.getNome(),
                 prod.getQtdProduto()
             });
         }
    }

