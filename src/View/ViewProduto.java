        // TODO add your handling code here:
        Produto prod = new Produto();
        ProdutoDAO pdao = new ProdutoDAO();
        
        prod.setNome(jTNomeProduto.getText());
        prod.setQtdProduto(Integer.parseInt(jTqtdProduto.getText()));
        pdao.create(prod);
        
        jTNomeProduto.setText("");
